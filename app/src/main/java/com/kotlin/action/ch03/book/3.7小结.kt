//3.7 小结
//• Kotlin 没有定义自己的集合类，而是在Java 集合类的基础上提供了更丰富的API 。
//• Kotlin 可以给函数参数定义默认值，这样大大降低了重载函数的必要性， 而
//且命名参数让多参数函数的调用更加易读。
//• Kotlin 允许更灵活的代码结构：函数和属性都可以直接在文件中声明，而不
//仅仅是在类中作为成员。
//• Kotlin 可以用扩展函数和属性来扩展任何类的API ，包括在外部库中定义的类，
//而不需要修改其源代码，也没有运行时开销。
//． 中缀调用提供了处理单个参数的，类似调用运算符方法的简明语法。
//• Kotlin 为普通字符串和正则表达式都提供了大量的方便字符串处理的函数。
//· 三重引号的字符串提供了一种简洁的方式，解决了原本在Java 中需要进行大
//量唠唆的转义和宇符串连接的问题。
//· 局部函数帮助你保持代码整洁的同时，避免重复。